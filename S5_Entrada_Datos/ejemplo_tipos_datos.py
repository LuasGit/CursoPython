#Ejemplo de concatenacion o suma de valores

num_cadena1 = '23'
num_cadena2 = '45'
concatenacion = num_cadena1 + num_cadena2
print(f'La concatenacion seria: {concatenacion}')

#Convertimos a enteros o floats

num_entero1 = int(num_cadena1)
num_entero2 = int(num_cadena2)
suma = num_entero1 + num_entero2
print(f'La suma es: {suma}')